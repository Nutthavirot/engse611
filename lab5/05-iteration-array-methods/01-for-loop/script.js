// Example of a basic for loop that counts from 1 to 5
for (let i = 1; i <= 5; i++) {
    console.log("Count:", i);
  }
  
  // Example of a for loop to sum numbers from 1 to 10
  let sum = 0;
  for (let i = 1; i <= 10; i++) {
    sum += i; // Add the current number to sum
  }
  console.log("The sum of numbers from 1 to 10 is:", sum);
  
  // Example of a for loop to iterate over an array
  const fruits = ["apple", "banana", "cherry", "date"];
  for (let i = 0; i < fruits.length; i++) {
    console.log(`Fruit ${i + 1}:`, fruits[i]);
  }
  